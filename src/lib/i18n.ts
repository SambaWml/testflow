export const LANG_STORAGE_KEY = "testflow_lang";

export function getLang(): Lang {
  if (typeof window === "undefined") return "en-US";
  const stored = localStorage.getItem(LANG_STORAGE_KEY);
  return stored === "pt-BR" ? "pt-BR" : "en-US";
}

export function setLangAndReload(lang: Lang) {
  localStorage.setItem(LANG_STORAGE_KEY, lang);
  window.location.reload();
}

/** Pluralize helper: replaces {n} and {s} in a string */
export function pl(str: string, n: number): string {
  return str.replace("{n}", String(n)).replace("{s}", n === 1 ? "" : "s");
}
